package com.bosch.caltool.apic.ws.exception;

public class ApicWSException {
	/**
	 * Represents any exception thrown by APIC web service.
	 */
	public static final int WEB_SERVICE_EXCEPTION = -450001;
	/**
	 * Represents error code when file not found.
	 */
	public static final int FILE_NOT_FOUND = -450002;
	/**
	 * Represents error code when file is not accessible.
	 */
	public static final int FILE_NOT_ACCESSIBLE = -450003;
	/**
	 * Represents error code when file IO error occurs.
	 */
	public static final int IO_ERROR = -450004;
}
