public class EASEEServiceException extends Exception {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	public EASEEServiceException() {
		super();
	}

	public EASEEServiceException(String arg0) {
		super(arg0);
	}

	public EASEEServiceException(Throwable arg0) {
		super(arg0);
	}

	public EASEEServiceException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

}
