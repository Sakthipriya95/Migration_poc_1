CREATE OR REPLACE FORCE VIEW TA2L_VCDM_VERSIONS 
  (  "VERS_NUMMER"
   , "ORIGINAL_FILE"
   , "PST"
   , "VCDM_CHECKSUM"
   , "A2L_CHECKSUM"
   , "FILESIZE"
   , "VCDM_COMMENT"
   , "ORIGINAL_DATE"
  ) AS 
  select "VERS_NUMMER"
       , "ORIGINAL_FILE"
       , "PST"
       , "VCDM_CHECKSUM"
       , "A2L_CHECKSUM"
       , "FILESIZE"
       , "VCDM_COMMENT"
       , "ORIGINAL_DATE"
  from TA2L_VCDM_VERSIONS@DGSPRO.WORLD@K5ESK_VILLA_RO
;


